    private Integer validarNumero1()throws NumeroUnoException{
        Integer retorno = 0;
        try{
            retorno = Integer.parseInt(edtNumero1.getText());
            if (retorno < 0){
                 throw new NumeroUnoException();
            }
            return retorno;
        }catch(Exception e){
            if(retorno < 0){
                throw new NumeroUnoException(String.format("El valor %d debe de ser mayor a 0", retorno));
            }
            throw new NumeroUnoException(String.format("El valor %s no representa un numero", edtNumero1.getText()));
        }
    }
    
