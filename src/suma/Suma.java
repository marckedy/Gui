    private void btnSumarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSumarActionPerformed
       
        try{
            Integer uno = validarNumero1();
            Integer dos = validarNumero2();
            Integer tres = validarNumero3();
            Integer total = uno + dos + tres;
            JOptionPane.showMessageDialog(this, uno + " + " + dos + " + " + tres + " = " + total, "Suma", JOptionPane.DEFAULT_OPTION);
        }catch (NumeroUnoException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            edtNumero1.requestFocus();
        }catch (NumeroDosException e){
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            edtNumero2.requestFocus();
        }catch (NumeroTresException e){
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            edtNumero3.requestFocus();
        }
        
    }//GEN-LAST:event_btnSumarActionPerformed

